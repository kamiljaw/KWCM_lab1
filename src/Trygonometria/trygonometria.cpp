#include "trygonometria.h"
#include <cmath>
double degreemath::sin(double degree) {
 double radian = degree*3.14;
 return sin(radian);
}

double degreemath::cos(double degree) {
 double radian = degree*3.14;
 return cos(radian);
}

double degreemath::tan(double degree) {
 double radian = degree*3.14;
 return tan(radian);
}

 double degreemath::ctg(double degree) {
 double radian = degree*3.14;
 return 1/tan(radian);

}
